package sample;

// Calculator Class that use to inherit
public class Calculator {
	// Variables to store object of inherited class 
	int a;
	int b;

	// sum function
	public int sum() {
		return (a+b);
		// must return something in INT case
	}	

	// multiply function
	public int mul() {
		return (a*b);
		// must return something in INT case
	}
	
	// subtract function
	public int sub() {
		return (a-b);
		// must return something in INT case
	}
	
	// divide function
	public int div() {
		return (a/b);
		// must return something in INT case
	}
	
}
